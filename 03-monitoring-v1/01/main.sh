#!/bin/bash


# Проверяем, что передан ровно один параметр
if [ $# -ne 1 ]; then
    echo "Ошибка: введите в скрипт только 1 параметр"
    echo "Как использовать: $0 <text_parameter>"
    exit 1
fi

# Сохраняем параметр в переменную
input="$1"

# Проверяем разные варианты "чисел"
if [[ "$input" =~ ^[+-]?[0-9]+$ ]]; then
    # Целое число (может быть со знаком + или -)
    echo "Ошибка: Параметр '$input' является числом. Некорректный ввод."
    exit 1
elif [[ "$input" =~ ^[+-]?[0-9]*\.?[0-9]+$ ]]; then
    # Вещественное число (десятичное)
    echo "Ошибка: Параметр '$input' является числом. Некорректный ввод."
    exit 1
elif [[ "$input" =~ ^[+-]?[0-9]+\.[0-9]*$ ]]; then  # для чисел вида "12."
    echo "Ошибка: Параметр '$input' является числом. Некорректный ввод."
    exit 1    
fi

# Если мы дошли сюда, значит параметр не является числом
# Выводим значение параметра
echo "$input"

# Завершаем скрипт успешно
exit 0